					<li class="releases">

						<h3>Plastic Clichés - Automaticity</h3>

						<h4>Released on <time datetime='2017-02-08'>February 8, 2017</time></h4>

						<div class="containersreleases">	

							<iframe title="Automaticity on Bandcamp" class="iframebc" src="https://bandcamp.com/EmbeddedPlayer/album=767604458/size=large/bgcol=EFEBEB/linkcol=5E70BA/transparent=true/"></iframe>

							<p>The first Plastic Clichés EP. This EP could have been called "Earliest Clichés", since I made those tracks between 2009 and 2012. During that time I was making a few different genres of music, like House, <a href="https://soundcloud.com/turborecordings/d-i-m-airbus-baby">rough banging Techno</a>, some Pop etc. I felt like I constrained myself way too much by always trying to go for a specific genre. So one day in 2009, I just decided to make the music I wanted to make, without restraining myself in any way. I sampled my beloved SH-101 into my -then recently acquired- EPS sampler, and started making a track using the same SH101 lead sample (and a phaser) for all the parts. A few hours later, the track <a href="https://soundcloud.com/plasticcliches/automaticity1">"Automaticity"</a> was born. I liked it so much that I decided to do other tracks in that style : <a href="https://www.youtube.com/watch?v=LSIr1LO6sVk">the style of no style</a>, as Bruce Lee would say. And I made lots of them in the years since that moment.</p>

							<p>On that fateful day, I learned that sometimes it's important to just have fun making music, have fun creating, and not think about what you're going to do or what you're looking to do too much. Sometimes it's how the best music comes to life.</p>

							<p>I also had an <a href="https://www.youtube.com/watch?v=yANsf_S_QJ4">official video</a> made for the "Automaticity" track. It's a great psychedelic video made by my friend <a href="https://soundcloud.com/laurencemcfunk">Laurence McFunk</a>. Thanks again Laurence !</p>

							<p>What's funny about the "Automaticity" track is that it took a few hours to create, but almost 8 years to release it officially ! So things were actually far from being automatic with this EP.</p>

						</div>

					</li>

					<li class="releases">

						<h3>Joy</h3>

						<h4>Released on <time datetime='2016-07-07'>July 7, 2016</time></h4>

						<div class="containersreleases">

							<iframe title="Joy on Bandcamp" class="iframebc" src="https://bandcamp.com/EmbeddedPlayer/album=2313290561/size=large/bgcol=EFEBEB/linkcol=5E70BA/transparent=true/"></iframe>

							<p>Between 2014 and 2016 was the time when I made lots of <a href="https://soundcloud.com/videovoid/sub-urban-video-void-remix?in=videovoid/sets/remixes">classically inspired Disco</a>. I realized how much this music meant to me -it was the first genre of music I really fell in love with as a child- so I wanted to make some Disco using all the tools I had learned to use since starting making music. The "Joy" EP was a natural result of that process.</p>

							<p>I have a particular fondness for the <a href="https://soundcloud.com/videovoid/stumbled-through-time?in=videovoid/sets/joy">"Stumbled Through Time"</a> opening song. Musically inspired by <a href="https://www.youtube.com/watch?v=M8b5gzm7SOQ">80's New York Funk</a>, it was an epic production with lots and lots of tracks. Lyrically, the song is about a man who goes back in time, but it doesn't go according to plan and he ends up in the wrong era, unconscious in his time machine. A girl finds him there and they fall in love. I had read about the <a href="https://en.wikipedia.org/wiki/Moberly%E2%80%93Jourdain_incident">Moberly–Jourdain incident</a>, where two English women claimed to have been transported to 18th century France -even seeing Marie Antoinette- while visiting <i>Le Petit Trianon</i>, near the Palace of Versailles. I thought it would be fun to write a love song based on time travel.</p>

							<p>I also have good memories of the making of <a href="https://soundcloud.com/videovoid/sailing-on-the-ocean-of-love">"Sailing on the Ocean of Love"</a>. I remember making it from start to finish in an epic 14-hour production session. The night before I had been partying quite hard at a house party, and I woke up at my friend's with a headache. I helped him clean up and went home.
							I'm glad I didn't let my terrible hangover stop me from creating this track when I got home.</p>

						</div>

					</li>

					<li class="releases">

						<h3>Early Traxx</h3>

						<h4 class="releaseinfo">Released on <time datetime='2014-09-27'>September 27, 2014</time></h4>

						<div class="containersreleases">

							<iframe title="Early Traxx on Bandcamp" class="iframebc iframe3trackeps" src="https://bandcamp.com/EmbeddedPlayer/album=2924548372/size=large/bgcol=EFEBEB/linkcol=5E70BA/transparent=true/"></iframe>

							<p>The first Video Void EP, inspired by mid-80's Italo Disco and then recent Synthwave/Dreamwave. The tracks are pretty early indeed, since they're from around 2010/2012. <a href="https://soundcloud.com/videovoid/welcome?in=videovoid/sets/early-traxx-ep">"Welcome"</a> is a bit of an homage to one of my favorite Italo tracks of all time, <a href="https://www.youtube.com/watch?v=T_Xjss0PX9g">"Void Vision"</a> by Cyber People, with its great vocoder lines !</p>

							<p>Apart from Italo, the main inspiration behind this EP was the works from <a href="http://valeriecollective.com/">Valérie Collective</a>. I followed their blog for a while (around 2008/2010) and loved some of their artists, such as <a href="https://www.youtube.com/watch?v=1uKTIo1vZtY">Russ Chimes</a>, <a href="https://www.youtube.com/watch?v=TY37Cc6j8UI">Anoraak</a>, and <a href="https://www.youtube.com/watch?v=2JfkSAJrmZk">Stephen Falken</a>. Those dreamy 80's synths were like crack to me. I even bought <a href="https://collegeoflove.bandcamp.com/album/secret-diary">College's first album</a> for my sister's birthday back in 2008. 
							Those guys were one of the biggest contemporary inspiration for my early sound, which borrowed a lot from the 80's.
							</p>
							

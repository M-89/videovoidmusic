							It's an exercise in catchy Nudisco, driven by a huge analog bassline and heavy drums.
							The B-side, <a href="https://soundcloud.com/videovoid/m-bisons-night-out?in=videovoid/sets/original-works">"M. Bison's Night Out"</a> is a psychedelic slow Disco ride wherein we follow M. Bison during the night out of his life in the New York City underground. For this track, I was inspired by <a href="https://www.youtube.com/watch?v=qSpXLCcqwuI">the DFA golden years</a> -for the percussions and <a href="https://www.youtube.com/watch?v=ERdGCgI-Uj4"> acidic lines</a>- and <a href="https://www.youtube.com/watch?v=MIwPuprGhmU">early 80's New York Boogie</a>, for the general funkiness and 80's synths. It actually started out as a cover from the <a href="https://www.youtube.com/watch?v=72aSGvXeOTs">M. Bison theme</a> from Street Fighter II (for the <a href="https://30thfloor.uk/album/synth-fighters">Synth Fighters compilation</a>), but quickly morphed into something else...</p>

